    firstIn.in = "23:59:59";
    lastOut.out = "00:00:00";

    for (int i = 0; i < n; ++i) {
        cin >> tmp.id >> tmp.in >> tmp.out;

        if(tmp.in < firstIn.in) {
            firstIn = tmp;
        }

        if(tmp.out > lastOut.out) {
            lastOut = tmp;
        }
    }

    cout << firstIn.id << " " << lastOut.id << endl;
