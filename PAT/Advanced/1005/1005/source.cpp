string voice[] = {
    "zero",
    "one",
    "two",
    "three",
    "four",
    "five",
    "six",
    "seven",
    "eight",
    "nine",
};

int get_sum(string number) {
    int sum = 0;

    for (auto digit : number) {
        sum += (digit - '0');
    }

    return sum;
}

void speak_it_out(int sum) {
    if(sum > 9) {
        speak_it_out(sum / 10);
        cout << " ";
    }

    cout << voice[sum % 10];
}

